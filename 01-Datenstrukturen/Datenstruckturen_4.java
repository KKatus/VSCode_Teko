public class Datenstruckturen_4 {

    public static void main(String[] args) {
        String a = "Hallo";
        int b = 4;
        float c = 1.5f;

        // Ausgabe b+c resultiert in einem Float
        System.out.println(b + c);
        // Ausgabe a + b resultiert in einem String
        System.out.print(a + b);
        // Ausgabe b * c als float
        System.out.print(b * c);
    }
}